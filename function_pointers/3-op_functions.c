/**
* op_add - get sum
* @a: integer a
* @b: integer b
* Return: the sums of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - get the difference of a and b
* @a: integer a
* @b: integer b
* Return: the difference of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - get the product
* @a: integer a
* @b: integer b
* Return: the product a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - get result of division
* @a: integer a
* @b: integer b
* Return: the result of division
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - get the remainder of the division
* @a: integer a
* @b: integer b
* Return: the remainder of the division of a by b
*/
int op_mod(int a, int b)
{
	return (a % b);
}

